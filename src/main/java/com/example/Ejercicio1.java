package com.example;

public class Ejercicio1 {
    public static void ejercicio1() {

        // Declaración de variables 
        int x = 10;
        int y = 5; 

        // Suma 
        int resultadoSuma = x+ y;
        System.out.println("La suma es: " + resultadoSuma);

        // Resta
        int resultadoResta = x - y; 
        System.out.println("La resta es: " + resultadoResta);

        // Multiplicación 
        int resultadoMultiplicacion = x*y;
        System.out.println("La multiplicación es: " + resultadoMultiplicacion);

        // División entera 
        int resultadoDivision = x / y;
        System.out.println("La división es: " + resultadoDivision);

        // Módulo
         int resultadoModulo = x % y;
         System.out.println("El módulo es: " + resultadoModulo);
    }
}
